  function normalizeSearchText(value) {
    return normalize(String(value || ""))
      .replaceAll("ё", "е")
      .replace(/\bposm\b/g, "pos материалы")
      .replace(/\broll\s*up\b/g, "rollup роллап")
      .replace(/\bpop\s*up\b/g, "popup попап")
      .replace(/\bpress\s*wall\b/g, "прессволл брендволл фотозона")
      .replace(/\blight\s*box\b/g, "лайтбокс световой короб")
      .replace(/\busb\b/g, "usb флешка накопитель")
      .replace(/&nbsp;/g, " ")
      .replace(/[«»"(){}\[\],.:;!?/\\|+*=~`^№-]+/g, " ")
      .replace(/\s+/g, " ")
      .trim();
  }

  function tokenize(value) {
    return normalizeSearchText(value)
      .split(" ")
      .filter((token) => token.length > 1 && !stopWords.has(token));
  }

  function stemToken(token) {
    return token
      .replace(/(иями|ями|ами|ого|ему|ыми|ими|ая|яя|ые|ие|ый|ий|ой|ое|ее|ам|ям|ах|ях|ов|ев|ом|ем|ую|юю|а|я|ы|и|е|у|ю|о)$/i, "")
      .slice(0, 18);
  }

  function getSearchVariants(value) {
    const tokens = tokenize(value);
    return [...new Set([...tokens, ...tokens.map(stemToken).filter((token) => token.length > 2)])];
  }

  function fixKeyboardLayout(value) {
    return normalize(String(value || ""))
      .split("")
      .map((char) => keyboardLayoutMap[char] || char)
      .join("");
  }

  function getQuerySearchTexts(value) {
    const original = normalizeSearchText(value);
    const keyboardFixed = normalizeSearchText(fixKeyboardLayout(value));

    return [...new Set([original, keyboardFixed].filter(Boolean))];
  }

  function getEditDistance(left, right) {
    if (Math.abs(left.length - right.length) > 2) {
      return 3;
    }

    const previous = Array.from({ length: right.length + 1 }, (_, index) => index);
    const current = Array.from({ length: right.length + 1 }, () => 0);

    for (let leftIndex = 1; leftIndex <= left.length; leftIndex += 1) {
      current[0] = leftIndex;

      for (let rightIndex = 1; rightIndex <= right.length; rightIndex += 1) {
        const cost = left[leftIndex - 1] === right[rightIndex - 1] ? 0 : 1;
        current[rightIndex] = Math.min(
          previous[rightIndex] + 1,
          current[rightIndex - 1] + 1,
          previous[rightIndex - 1] + cost,
        );
      }

      previous.splice(0, previous.length, ...current);
    }

    return previous[right.length];
  }

  function getFuzzyTokenScore(token, candidates) {
    if (token.length < 4) {
      return 0;
    }

    return candidates.some((candidate) => {
      if (candidate.length < 4 || Math.abs(candidate.length - token.length) > 2) {
        return false;
      }

      return getEditDistance(token, candidate) <= (token.length > 6 ? 2 : 1);
    })
      ? 5
      : 0;
  }

  function getMatchedIntentRules(title, section, catalogKind) {
    const haystack = normalizeSearchText(`${title} ${section}`);
    return searchIntentRules.filter((rule) => {
      const matchesTarget = rule.targets.some((target) => normalizeSearchText(target) === normalizeSearchText(title));
      const matchesAlias = rule.aliases.some((alias) => includesAny(haystack, getSearchVariants(alias)));

      return matchesTarget || matchesAlias;
    });
  }

  function buildSearchAliases(catalogKind, title, section) {
    const haystack = normalizeSearchText(`${title} ${section}`);
    const aliases = [
      title,
      section,
      catalogConfigs[catalogKind].label,
      catalogKind === "souvenirs" ? "сувениры промо мерч брендирование логотип подарок" : "полиграфия печать реклама изготовление дизайн",
      ...(exactServiceAliases[title] || []),
    ];

    serviceAliasRules.forEach((rule) => {
      if (rule.match.some((token) => haystack.includes(token))) {
        aliases.push(...rule.aliases);
      }
    });

    getMatchedIntentRules(title, section, catalogKind).forEach((rule) => {
      aliases.push(...rule.terms, ...rule.aliases);
    });

    return [...new Set(aliases)];
  }

  function buildSearchProfile(service) {
    const aliases = buildSearchAliases(service.catalogKind, service.title, service.section);
    const searchableText = [service.title, service.section, service.label, service.lead, aliases.join(" ")].join(" ");
    const titleTokens = getSearchVariants(service.title);
    const sectionTokens = getSearchVariants(service.section);
    const aliasTokens = getSearchVariants(aliases.join(" "));

    return {
      text: normalizeSearchText(searchableText),
      slugText: slugify(searchableText),
      titleTokens,
      sectionTokens,
      aliasTokens,
      exactPhrases: aliases.map(normalizeSearchText).filter(Boolean),
    };
  }

  function scoreService(service, query, preferredKind = "") {
    const queryTexts = getQuerySearchTexts(query);
    const normalizedQuery = queryTexts[0] || "";
    if (!normalizedQuery) {
      return 0;
    }

    const profile = service.searchProfile;
    const queryTokens = getSearchVariants(queryTexts.join(" "));
    const querySlugTexts = queryTexts.map(slugify).filter(Boolean);
    const queryContext = queryTexts.join(" ");
    const serviceContext = normalizeSearchText(`${service.title} ${service.section}`);
    let score = 0;

    if (preferredKind && service.catalogKind === preferredKind) {
      score += 8;
    }

    if (queryTexts.some((queryText) => normalizeSearchText(service.title) === queryText)) {
      score += 120;
    }

    if (queryTexts.some((queryText) => normalizeSearchText(service.title).includes(queryText))) {
      score += 75;
    }

    if (querySlugTexts.some((querySlug) => querySlug.length > 2 && profile.slugText.includes(querySlug))) {
      score += 80;
    }

    if (
      profile.exactPhrases.some((phrase) =>
        queryTexts.some((queryText) => phrase && (phrase.includes(queryText) || queryText.includes(phrase))),
      )
    ) {
      score += 45;
    }

    queryTokens.forEach((token) => {
      if (profile.titleTokens.includes(token)) {
        score += 28;
      } else if (profile.titleTokens.some((candidate) => candidate.startsWith(token) || token.startsWith(candidate))) {
        score += 20;
      }

      if (profile.sectionTokens.includes(token)) {
        score += 14;
      }

      if (profile.aliasTokens.includes(token)) {
        score += 11;
      } else if (profile.text.includes(token)) {
        score += 6;
      } else {
        score += getFuzzyTokenScore(token, profile.aliasTokens);
      }
    });

    searchIntentRules.forEach((rule) => {
      const matchedTerms = rule.terms.filter((term) => queryContext.includes(normalizeSearchText(term)) || queryTokens.includes(stemToken(normalizeSearchText(term))));
      if (!matchedTerms.length) {
        return;
      }

      if (rule.targets.some((target) => normalizeSearchText(target) === normalizeSearchText(service.title))) {
        score += 42 + matchedTerms.length * 8;
        return;
      }

      if (rule.aliases.some((alias) => profile.exactPhrases.includes(normalizeSearchText(alias)))) {
        score += 22 + matchedTerms.length * 4;
      }
    });

    if (includesAny(queryContext, ["подар", "мерч", "корпоратив"]) && serviceContext.includes("подароч")) {
      score += 55;
    }

    if (includesAny(queryContext, ["упаков", "товар", "маркиров"]) && includesAny(serviceContext, ["этикет", "самокле", "наклеек на самокле"])) {
      score += 70;
    }

    if (includesAny(queryContext, ["упаков", "товар", "маркиров"]) && serviceContext.includes("этикет")) {
      score += 90;
    }

    if (queryContext.includes("наклей") && includesAny(queryContext, ["упаков", "товар", "маркиров"]) && service.section === "Самоклеющаяся продукция") {
      score += 45;
    }

    if (queryContext.includes("наклей") && includesAny(queryContext, ["упаков", "товар", "маркиров"]) && serviceContext.includes("подставк")) {
      score -= 80;
    }

    if (includesAny(queryContext, ["упаков", "товар", "маркиров"]) && serviceContext.includes("авто")) {
      score -= 90;
    }

    if (includesAny(queryContext, ["магазин", "полк", "торгов", "товар"]) && service.section === "Оформление мест продаж") {
      score += 50;
    }

    if (includesAny(queryContext, ["магазин", "полк", "торгов", "товар"]) && service.section === "Наружная реклама" && !queryContext.includes("витрин")) {
      score -= 70;
    }

    if (queryContext.includes("выстав") && service.section === "Оформление выставок") {
      score += 45;
    }

    if (queryContext.includes("фото") && includesAny(queryContext, ["стен", "интерьер"]) && includesAny(serviceContext, ["фотообо", "холст", "фотограф"])) {
      score += 55;
    }

    if (queryContext.includes("фото") && includesAny(queryContext, ["стен", "интерьер"]) && serviceContext.includes("стикер")) {
      score -= 45;
    }

    if (includesAny(queryContext, ["шоппер", "экосумк", "tote bag"]) && serviceContext.includes("тканевых сумках")) {
      score += 120;
    }

    if (includesAny(queryContext, ["шоппер", "экосумк", "tote bag"]) && serviceContext.includes("логотипом") && !serviceContext.includes("сумк")) {
      score -= 70;
    }

    if (includesAny(queryContext, ["rollup", "роллап", "ролл ап", "выдвижной стенд"]) && serviceContext.includes("roll up")) {
      score += 140;
    }

    if (includesAny(queryContext, ["rollup", "роллап", "ролл ап"]) && service.title === "Спецпредложение «Выставка под ключ»") {
      score -= 80;
    }

    if (includesAny(queryContext, ["наклейка на машину", "наклейки на машину", "автонаклей", "оклейка авто", "реклама на машине"]) && serviceContext.includes("наклейки на авто")) {
      score += 140;
    }

    if (includesAny(queryContext, ["наклейка на машину", "наклейки на машину", "автонаклей", "оклейка авто", "реклама на машине"]) && service.section === "Самоклеющаяся продукция") {
      score -= 90;
    }

    return score;
  }

  function includesAny(value, variants) {
    return variants.some((variant) => value.includes(variant));
  }

  function slugify(value) {
    const transliterated = normalize(value)
      .split("")
      .map((char) => translitMap[char] ?? char)
      .join("");

    return transliterated
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "")
      .replace(/-{2,}/g, "-");
  }

