  function getCatalog(kind) {
    const config = catalogConfigs[kind];
    if (!config) {
      return null;
    }

    const items = services.filter((item) => item.catalogKind === kind);
    const sectionCategories = config.categories?.length
      ? config.categories
          .map((category) => ({
            id: category.id,
            label: category.label,
            count: items.filter((item) => item.categoryId === category.id).length,
          }))
          .filter((category) => category.count > 0)
      : Object.entries(
          items.reduce((acc, item) => {
            acc[item.section] = (acc[item.section] || 0) + 1;
            return acc;
          }, {}),
        )
          .sort((left, right) => left[0].localeCompare(right[0], "ru"))
          .map(([label, count]) => ({
            id: slugify(label),
            label,
            count,
          }));

    const categories =
      sectionCategories.length === 1 && sectionCategories[0].count === items.length
        ? sectionCategories
        : [{ id: "all", label: config.allLabel, count: items.length }, ...sectionCategories];

    return {
      ...config,
      kind,
      items,
      categories,
      defaultCategory: categories[0]?.id || "all",
    };
  }

  function findService({ slug = "", title = "", kind = "" } = {}) {
    const normalizedSlug = normalize(slug);
    const normalizedTitle = normalize(title);

    return (
      services.find((item) => item.catalogKind === kind && item.slug === normalizedSlug) ||
      services.find((item) => item.catalogKind === kind && normalize(item.title) === normalizedTitle) ||
      services.find((item) => item.slug === normalizedSlug) ||
      services.find((item) => normalize(item.title) === normalizedTitle) ||
      null
    );
  }

  function searchAll(query, { kind = "", limit = 12, minScore = 16 } = {}) {
    const normalizedQuery = normalizeSearchText(query);

    if (!normalizedQuery) {
      return [];
    }

    return services
      .filter((item) => !kind || item.catalogKind === kind)
      .map((item) => ({
        item,
        score: scoreService(item, normalizedQuery, kind),
      }))
      .filter((result) => result.score >= minScore)
      .sort((left, right) => right.score - left.score || right.item.popularity - left.item.popularity)
      .slice(0, limit)
      .map((result) => result.item);
  }

  function search(query, options = {}) {
    return searchAll(query, { ...options, limit: 1 })[0] || null;
  }

